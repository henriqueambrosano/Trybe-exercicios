// somar apenas os elementos pares de um array com a função reduce

const myArray = [1, 2, 3];

console.log(myArray.reduce((a, b) => (b % 2 === 0 ? a + b : a), 0));

// somar apenas os elementos pares de um array com a função filter e reduce

console.log(myArray.filter((el) => el % 2 === 0).reduce((a, b) => a + b));

